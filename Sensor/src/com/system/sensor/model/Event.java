package com.system.sensor.model;

public class Event {
	
	private Long sensorId;
	private Long tag;
	
	//Getters & Setters
	public Long getSensorId() {
		return sensorId;
	}
	public void setSensorId(Long sensorId) {
		this.sensorId = sensorId;
	}
	public Long getTag() {
		return tag;
	}
	public void setTag(Long tag) {
		this.tag = tag;
	}

}
